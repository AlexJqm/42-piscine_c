#include <stdlib.h>

int	ft_ultimate_range(int **range, int min, int max)
{
	int *tab;
	int i;

	i = 0;
	tab = 0;
	if (min >= max)
		return (0);
	if (!(tab = (int*)malloc((max - min + 1) * sizeof(int))))
		return (0);
	while (min < max)
	{
		tab[i] = min;
		min++;
		i++;
	}
	*range = tab;
	return (i);
}
