int	ft_atoi(char *str)
{
	int i;
	int j;

	i = 0;
	j = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32 || *str == 127)
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			j = -1;
		str++;
	}
	while (*str != '\0' && *str >= '0' && *str <= '9')
	{
		i = i * 10 + (*str - 48);
		str++;
	}
	return (i * j);
}
