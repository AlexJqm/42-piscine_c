int		ft_str_is_lowercase(char *str)
{
	int j;

	j = 0;
	while (str[j] != '\0')
	{
		if ((str[j] >= 0 && str[j] <= 96) || (str[j] >= 123 && str[j] <= 127))
			return (0);
		j++;
	}
	return (1);
}
